// The site documents the whole Anticapture API surface: the Gateful REST API,
// the MCP server (whose tools are generated by Kubb from the same Gateful
// OpenAPI spec rendered here - operationId -> tool name, tag -> tool group),
// and the standalone Webhook Notification API.
//
// Both spec paths point at filtered copies produced by
// `scripts/prepare-spec.mjs` (run before gen-api-docs), which strips the
// unsupported relayer endpoints and internal marker tags from the Gateful
// spec. They are gitignored and regenerated on every build.
const GATEFUL_SPEC = "./openapi/gateful.json";
const WEBHOOK_SPEC = "./openapi/webhook.json";

